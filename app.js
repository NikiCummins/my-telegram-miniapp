const tg = window.Telegram.WebApp;
let isRunning = false;

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
function initApp() {
    console.log('Simple Control App –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω');
    tg.expand(); // –†–∞—Å—à–∏—Ä—è–µ–º –Ω–∞ –≤–µ—Å—å —ç–∫—Ä–∞–Ω
    updateUI();
}

// –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ —Å–æ—Å—Ç–æ—è–Ω–∏—è Start/Stop
function toggleControl() {
    isRunning = !isRunning;
    
    // –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏
    const controlData = {
        action: "control_toggle",
        state: isRunning ? "start" : "stop",
        user_id: tg.initDataUnsafe.user?.id,
        user_name: tg.initDataUnsafe.user?.first_name || "User",
        timestamp: new Date().toISOString()
    };
    
    console.log("üì§ –û—Ç–ø—Ä–∞–≤–∫–∞ –¥–∞–Ω–Ω—ã—Ö –≤ –±–æ—Ç–∞:", controlData);
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –≤ –±–æ—Ç–∞
    tg.sendData(JSON.stringify(controlData));
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å
    updateUI();
    
    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
    tg.showAlert(isRunning ? "‚úÖ –°–∏—Å—Ç–µ–º–∞ –∑–∞–ø—É—â–µ–Ω–∞!" : "üõë –°–∏—Å—Ç–µ–º–∞ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞!");
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞
function updateUI() {
    const statusElement = document.getElementById('status');
    const buttonElement = document.getElementById('controlBtn');
    
    if (isRunning) {
        statusElement.textContent = "–°—Ç–∞—Ç—É—Å: –ó–∞–ø—É—â–µ–Ω–∞ üü¢";
        statusElement.style.color = "#28a745";
        buttonElement.textContent = "üõë Stop";
        buttonElement.classList.add('stop');
    } else {
        statusElement.textContent = "–°—Ç–∞—Ç—É—Å: –û—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞ üî¥";
        statusElement.style.color = "#dc3545";
        buttonElement.textContent = "üöÄ Start";
        buttonElement.classList.remove('stop');
    }
}

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ
document.addEventListener('DOMContentLoaded', initApp);
